"""
카메라 테스트 유틸리티 모듈

이 스크립트는 시스템에 연결된 모든 카메라를 감지하고 테스트하기 위한 도구입니다.
인덱스 0부터 9까지의 카메라를 순차적으로 확인하고, 연결된 각 카메라에서 
테스트 프레임을 캡처하여 화면에 표시합니다.

사용 방법:
    python camera_test.py

종속성:
    - OpenCV (cv2)
"""

import cv2  # 컴퓨터 비전 라이브러리 임포트

def test_cameras():
    """
    시스템의 모든 사용 가능한 카메라 장치를 감지하고 테스트합니다.
    
    인덱스 0부터 9까지의 카메라에 연결을 시도합니다.
    각 카메라에 성공적으로 연결되면:
    1. 프레임을 캡처합니다
    2. 캡처된 이미지를 창으로 표시합니다
    3. 사용자 입력을 기다립니다
    4. 창을 닫고 다음 카메라로 넘어갑니다
    
    OpenCV의 waitKey(0)를 사용하여 사용자가 아무 키나 누를 때까지 대기합니다.
    """
    # 여러 카메라 인덱스를 0부터 9까지 테스트 (일반적으로 대부분의 시스템은 이 범위 내에 카메라가 있음)
    for index in range(10):
        # 지정된 인덱스로 카메라 캡처 객체 생성
        cap = cv2.VideoCapture(index)
        
        # 카메라가 성공적으로 열렸는지 확인
        if cap.isOpened():
            # 카메라에서 프레임 읽기 시도
            ret, frame = cap.read()
            
            # 프레임이 성공적으로 읽혔는지 확인
            if ret:
                print(f"카메라 인덱스 {index}가 작동합니다!")
                
                # 읽은 프레임을 표시할 창 생성
                window_name = f"카메라 {index} 테스트"
                cv2.imshow(window_name, frame)
                
                # 사용자에게 안내 메시지 출력
                print(f"아무 키나 눌러 {window_name} 창을 닫으세요")
                
                # 사용자가 키를 누를 때까지 대기 (0은 무한정 대기)
                cv2.waitKey(0)
                
                # 특정 창만 닫기
                cv2.destroyWindow(window_name)
            else:
                # 카메라는 열렸지만 프레임을 읽지 못한 경우
                print(f"카메라 {index}에 연결되었지만 프레임을 읽을 수 없습니다")
            
            # 카메라 리소스 해제 (중요: 항상 사용 후 해제해야 함)
            cap.release()
        else:
            # 카메라에 연결조차 되지 않은 경우
            print(f"카메라 인덱스 {index}에 연결할 수 없습니다")

# 스크립트가 직접 실행될 때만 아래 코드 실행
if __name__ == "__main__":
    print("카메라 테스트 시작...")
    test_cameras()  # 테스트 함수 호출
    print("카메라 테스트 완료")